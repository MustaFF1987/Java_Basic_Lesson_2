public class Calculator {

    public int mult(int x, int y) {

        int result = x * y;

        return result;
    }

    public double CirclePerimetr(double x) {

        double Perimetr_result = 2*3.14*x;

        return Perimetr_result;

    }
    public double CircleArea(double x) {

        double Area_result = 3.14 * (x*x) ;

        return Area_result;

    }
    public double sum(double x, double y) {

        double sum_result = x + y;

        return sum_result;

    }
    public double sub(double x, double y) {

        double sub_result = x - y;

        return sub_result;

    }

    public double div(double x, double y) {

        double div_result = x / y;

        return div_result;

    }

    public double multiply(double x, double y) {

        double multiply_result = x * y;

        return multiply_result;

    }

    public double average(double x, double y, double z) {

        double average_result = (x * y * z)/3;

        return average_result;

    }
}